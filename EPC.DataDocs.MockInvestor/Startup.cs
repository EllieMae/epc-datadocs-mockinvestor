namespace EPC.DataDocs.MockInvestor
{
    public class Startup
    {
        public Startup(IHostingEnvironment env)
        {
            var builder = new ConfigurationBuilder()
                .SetBasePath(env.ContentRootPath)
                .AddJsonFile("appsettings.json", optional: false, reloadOnChange: true)
                .AddJsonFile($"appsettings.{env.EnvironmentName}.json", optional: true)
                .AddEnvironmentVariables();
            Configuration = builder.Build();
        }

        public IConfiguration Configuration { get; }

        // This method gets called by the runtime. Use this method to add services to the container.
        public void ConfigureServices(IServiceCollection services)
        {
            var appSettings = Configuration.GetSection("AppSettings");

            // TODO : IOptions is currently tightly coupled with all the classes, when there is a change in the json , then it impacts many places - change this design.

            services.Configure<AppSettings>(appSettings);
            services.AddOptions();

            services.AddCors(o => o.AddPolicy("EnableCORSPolicy", builder =>
            {
                builder.AllowAnyOrigin()
                       .AllowAnyMethod()
                       .AllowAnyHeader();
            }));

            // Add framework services.
            services.AddMvc();

            services.AddScoped<IHttpContextAccessor, HttpContextAccessor>();

        }

        // This method gets called by the runtime. Use this method to configure the HTTP request pipeline.
        public void Configure(IApplicationBuilder app, IHostingEnvironment env, ILoggerFactory loggerFactory)
        {
            var loggingSection = Configuration.GetSection("Logging");
            loggerFactory.AddConsole(Configuration.GetSection("Logging"));
            loggerFactory.AddFile(Path.Combine(loggingSection["TraceLogLocation"].ToString(), "EPC_MockInvestor-{Date}.txt"));
            loggerFactory.AddDebug();

            //call ConfigureLogger in a centralized place in the code
            LogHelper.ConfigureLogger(loggerFactory);
            //set it as the primary LoggerFactory to use everywhere
            LogHelper.LoggerFactory = loggerFactory;

            // this has been added temporarily
            app.UseCors("EnableCORSPolicy");

            app.UseRequestResponseLogging();


            app.UseMvc();


        }
    }
}
